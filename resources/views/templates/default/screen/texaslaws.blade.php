<h4 class="bg-yellow">How can you carry a gun? </h4>

<h2>Handguns </h2>
<p>
People in Texas must carry a handgun in a holster. The holster does not have to be a shoulder or belt holster. Before the law changed in 2021, people in Texas needed to have a license to carry a handgun and they needed to keep their handgun in a shoulder or belt holster. 

Section 46.02 of the Texas Penal Code requires a handgun to be carried in a holster, but it does not provide a definition of the word "holster." 
</p>
<h4>Texas Laws </h4>
<p>
