<h2>Handguns </h2>

As of 2021, people who qualify under the law can carry a handgun in a public place in Texas without a license to carry (LTC). Texans can still apply for an LTC since it may carry additional benefits. See our License to Carry page for more info. 

Sections 46.02 and 46.04 of the Texas Penal Code describe unlawful carry and possession of a firearm. Generally, to carry a handgun in public in Texas without an LTC, a person must: 
<li class-"pt-2">Be at least 21 years old  </li>
<li>Not have a prior felony conviction as described in Texas Penal Code Section 46.04 </li>

<li>Not have a recent conviction for certain types of misdemeanors as described in Texas Penal Code Sections 46.02 and 46.04 </li>
<li>Not be subject to an unexpired protective order as described in Texas Penal Code Section 46.04(c)</li>
<li>Not be restricted from possessing a firearm under federal law as described in 18 United States Code Section 922(g)</li>
 
<li>Not be intoxicated, except in certain situations as described in Texas Penal Code Section 46.02(a-6) </li>

<p>
 The new Texas law does not give the right to carry a handgun to anyone who was prohibited before the new law took effect. Section 2 of HB 1927 states: 

</p>



